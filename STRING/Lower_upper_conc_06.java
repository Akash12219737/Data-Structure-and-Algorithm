package STRING;

public class Lower_upper_conc_06 {
    public static void main(String[] args) {
        String s = "Akash is Powerful person";
        // s.toLowerCase(); -> Nothing print
        s.toLowerCase(); //option
        System.out.println(s.toLowerCase());

        // UPPER CASE
        System.out.println(s.toUpperCase());

        // CONCATNATE

        String a = "Akash";
        String b = "Tech enthusiam";
        System.out.println(a.concat(b));

    }
    
}
